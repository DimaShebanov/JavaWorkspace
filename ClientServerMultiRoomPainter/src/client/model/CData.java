package client.model;
import java.awt.Color;

public class CData
{
	public int stroke = 5;
	public int number = 0;
	public int type = 3;
	public Color col = Color.black;
	
	
	public CData(int stroke, int type, Color col, int number)
	{
		this.stroke = stroke;
		this.number = number;
		this.type = type;
		this.col = col;
	}
	
	public CData()
	{}
	
	public CData dublicate()
	{
		return new CData(stroke, type, col, number);
	}

	
}
